package Activity5;

// 1. Write a program to print numbers from 1 to 10.
public class Number1 {
    public static void main(String[] args) {
        // Loop through numbers 1 to 10 (inclusive)
        for (int i = 1; i <= 10; i++) {
            // Print each number on a new line
            System.out.println(i);
        }
    }
}

package Activity5;

/*
 * Number 1
 * This program prints numbers from 1 to 10 using a for loop.
 * It demonstrates basic loop iteration and console output.
 * Input: None
 * Output: Numbers 1 through 10, each on a separate line
 */
public class Number1 {
    public static void main(String[] args) {
        // Loop through numbers 1 to 10 (inclusive)
        for (int i = 1; i <= 10; i++) {
            // Print each number on a new line
            System.out.println(i);
        }
    }
}

package Activity2;
public class ActivityNo2 {

    public static void main(String[] args) {
      // radius
      double r = 3; 
      // calculation for PI and r^3
      double pi = 3.14;
      double rCubed = r * r * r;
      double vSphere = (4.0 / 3.0) * pi * rCubed;
      System.out.printf("Volume of Sphere: %.2f%n", vSphere);
  }
}
